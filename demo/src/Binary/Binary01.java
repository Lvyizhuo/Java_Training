// 四种进制的演示
public class Binary01
{
    public static void main(String[] args)
    {
        // 十进制
        int a = 23;
        //二进制
        int b = 0b10111;
        //八进制
        int c = 027;
        //十六进制
        int d = 0x17;
        System.out.println("23" + a);
        System.out.println("" + b);
        System.out.println("" + c);
        System.out.println("" + d);


    }
}
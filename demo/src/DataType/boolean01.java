public class boolean01
{
    public static void main(String[] args)
    {
        boolean  a = true;
        boolean b = false;
        if (a == true)
        {
            System.out.println("a == true");
        }else {
            System.out.println("a == false");
        }

        if (b == true)
        {
            System.out.println("b == true");
        }else  {
            System.out.println("b == false");
        }
    }
}